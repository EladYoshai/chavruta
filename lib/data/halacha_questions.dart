  /// Returns a random quiz of 5 questions: 1 easy + 2 medium + 2 hard.
  /// New random selection each time.
  static List<QuizQuestion> getDailyQuiz(int seed) {
    final random = Random();

    final easy = questions.where((q) => q.difficulty == 1).toList();
    final medium = questions.where((q) => q.difficulty == 2).toList();
    final hard = questions.where((q) => q.difficulty == 3).toList();

    easy.shuffle(random);
    medium.shuffle(random);
    hard.shuffle(random);

    final selected = <QuizQuestion>[];
    selected.addAll(easy.take(1));
    selected.addAll(medium.take(2));
    selected.addAll(hard.take(2));
    selected.shuffle(random);

    return selected;
  }
